/** Convert full application to summary (for list views, masks phone) */
export function toApplicationSummary(
  app: OnboardingApplication,
): ApplicationSummary {
  return {
    application_id: app.application_id,
    phone_masked: app.phone.replace(/(\d{2})\d{6}(\d{2})/, "$1XXXXXX$2"),
    owner_name: app.owner_name,
    shop_name: app.shop_name,
    sector_name: app.sector_name,
    city: app.city,
    state: app.state,
    status: app.status,
    current_step: app.current_step,
    commission_rate: app.commission_rate,
    channel: app.channel,
    exec_name: app.exec_name,
    created_at: app.created_at,
    updated_at: app.updated_at,
    submitted_at: app.submitted_at,
  };
}

